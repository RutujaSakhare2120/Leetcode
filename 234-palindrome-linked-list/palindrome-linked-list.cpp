/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
  ListNode* reverseList(ListNode* head) {

        ListNode* next=head;
        ListNode* curr=head;
        ListNode* prev=NULL;

        while(next!=NULL){
          next=curr->next;
          curr->next=prev;
          prev=curr;
          curr=next;
        }

        return prev;
    }
    bool isPalindrome(ListNode* head) {
        //deep copy
        ListNode* c=new ListNode(500);
        ListNode* t=head;
        ListNode* tc=c;
        while(t){
          ListNode* node=new ListNode(t->val);
          tc->next=node;
          tc=node;
          t=t->next;

        }
        c=c->next;
        c=reverseList(c);

        ListNode* a=head;
        ListNode*b=c;
        while(a){
            if(a->val!=b->val) return false;
            a=a->next;
            b=b->next;
        }
        return true;
        
    }
};